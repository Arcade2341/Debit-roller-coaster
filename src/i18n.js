function getLanguage(lang) {
  return lang === "en" ? "en" : "fr";
}

function translate(lang, key, params = {}) {
  const normalizedLang = getLanguage(lang);
  const value = key.split(".").reduce((accumulator, part) => (accumulator ? accumulator[part] : undefined), messages[normalizedLang]);

  if (typeof value !== "string") {
    return key;
  }

  const pluralParts = value.split("|");
  const baseValue =
    pluralParts.length > 1 && typeof params.count !== "undefined"
      ? Number(params.count) > 1
        ? pluralParts[1]
        : pluralParts[0]
      : value;

  return baseValue.replace(/\{(\w+)\}/g, (_, name) => (typeof params[name] !== "undefined" ? params[name] : ""));
}

module.exports = {
  getLanguage,
  messages,
  translate
};
